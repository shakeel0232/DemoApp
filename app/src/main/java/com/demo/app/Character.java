package com.demo.app;

class Character {
    String CharName;
    String CharAge;
    String CharUrl;

    public String getCharName() {
        return CharName;
    }

    public void setCharName(String charName) {
        CharName = charName;
    }

    public String getCharAge() {
        return CharAge;
    }

    public void setCharAge(String charAge) {
        CharAge = charAge;
    }

    public String getCharUrl() {
        return CharUrl;
    }

    public void setCharUrl(String charUrl) {
        CharUrl = charUrl;
    }

}
